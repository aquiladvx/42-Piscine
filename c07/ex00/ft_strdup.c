#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int		ft_strlen(char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	char *destiny;

	destiny = malloc(ft_strlen(src) * sizeof(char *));
	if (destiny == NULL)
		return (0);
	ft_strcpy(destiny, src);
	return (destiny);
}
