void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int rev[size];

	i = 0;
	j = (size - 1);
	while (i < size)
	{
		rev[j] = *(tab + i);
		i++;
		j--;
	}
	j++;
	while (j < size)
	{
		*(tab + j) = rev[j];
		j++;
	}
}
