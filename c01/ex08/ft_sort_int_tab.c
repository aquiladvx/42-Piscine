void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int sort;

	i = 0;
	while (i < size)
	{
		if (tab[i] > tab[i + 1])
		{
			sort = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = sort;
			i = 0;
		}
		else
		{
			i++;
		}
	}
}
