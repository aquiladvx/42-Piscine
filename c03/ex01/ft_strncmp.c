int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				res;

	i = 0;
	while (i < n && s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			res = s1[i] - s2[i];
			return (res);
		}
		i++;
	}
	if (i < n)
	{
		if (s1[i] != '\0')
			return (s1[i]);
		if (s2[i] != '\0')
			return (-s2[i]);
	}
	return (0);
}
