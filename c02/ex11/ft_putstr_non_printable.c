void	ft_putstr_non_printable(char *str)
{
	*str = *str - 32;
	while (*str != '\0')
	{
		if ((*str > 31 && *str < 48) || (*str > 57 && *str < 65) || \
			(*str > 90 && *str < 97) || (*str > 122 && *str < 127))
		{
			str++;
			if ((*str > 96) && (*str < 123))
			{
				*str = *str - 32;
			}
		}
		str++;
	}
}
